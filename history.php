<div class="text">
        After its inauguration on January 28, 1950, the Supreme Court
        commenced its sittings in a part of the Parliament House. The Court
        moved into the present building in 1958. The building is shaped to
        project the image of scales of justice. The Central Wing of the building
        is the Centre Beam of the Scales. In 1979, two New Wings - the East Wing
        and the West Wing - were added to the complex. In all there are 15 Court
        Rooms in the various wings of the building. The Chief Justice's Court is
        the largest of the Courts located in the Centre of the Central Wing. The
        original Constitution of 1950 envisaged a Supreme Court with a Chief
        Justice and 7 puisne Judges - leaving it to Parliament to increase this
        number. In the early years, all the Judges of the Supreme Court sat
        together to hear the cases presented before them. As the work of the
        Court increased and arrears of cases began to cumulate, Parliament
        increased the number of Judges from 8 in 1950 to 11 in 1956, 14 in 1960,
        18 in 1978 and 26 in 1986. As the number of the Judges has increased,
        they sit in smaller Benches of two and three - coming together in larger
        Benches of 5 and more only when required to do so or to settle a
        difference of opinion or controversy. The Supreme Court of India
        comprises the Chief Justice and 30 other Judges appointed by the
        President of India. Supreme Court Judges retire upon attaining the age
        of 65 years. In order to be appointed as a Judge of the Supreme Court, a
        person must be a citizen of India and must have been, for atleast five
        years, a Judge of a High Court or of two or more such Courts in
        succession, or an Advocate of a High Court or of two or more such Courts
        in succession for at least 10 years or he must be, in the opinion of the
        President, a distinguished jurist. Provisions exist for the appointment
        of a Judge of a High Court as an Ad-hoc Judge of the Supreme Court and
        for retired Judges of the Supreme Court or High Courts to sit and act as
        Judges of that Court. The Constitution seeks to ensure the independence
        of Supreme Court Judges in various ways. A Judge of the Supreme Court
        cannot be removed from office except by an order of the President passed
        after an address in each House of Parliament supported by a majority of
        the total membership of that House and by a majority of not less than
        two-thirds of members present and voting, and presented to the President
        in the same Session for such removal on the ground of proved
        misbehaviour or incapacity. A person who has been a Judge of the Supreme
        Court is debarred from practising in any court of law or before any
        other authority in India. The proceedings of the Supreme Court are
        conducted in English only. Supreme Court Rules, 1966 are framed under
        Article 145 of the Constitution to regulate the practice and procedure
        of the Supreme Court.</p>
