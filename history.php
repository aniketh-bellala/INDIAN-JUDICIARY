  <div class="text">
      <p>Supreme Court of India came into existence on 26th January, 1950 and is
        located on Tilak Marg, New Delhi. The Supreme Court of India functioned
        from the Parliament House till it moved to the present building. It has
        a 27.6 metre high dome and a spacious colonnaded verandah. For a peek
        inside, you'll have to obtain a visitor's pass from the front office. On
        the 28th of January, 1950, two days after India became a Sovereign
        Democratic Republic, the Supreme Court came into being. The inauguration
        took place in the Chamber of Princes in the Parliament building which
        also housed India's Parliament, consisting of the Council of States and
        the House of the People. It was here, in this Chamber of Princes, that
        the Federal Court of India had sat for 12 years between 1937 and 1950.
        This was to be the home of the Supreme Court for years that were to
        follow until the Supreme Court acquired its own present premises. The
        inaugural proceedings were simple but impressive. They began at 9.45
        a.m. when the Judges of the Federal Court - Chief Justice Harilal
        J.Kania and Justices Saiyid Fazl Ali, M. Patanjali Sastri, Mehr Chand
        Mahajan, Bijan Kumar Mukherjea and S.R.Das - took their seats. In
        attendance were the Chief Justices of the High Courts of Allahabad,
        Bombay, Madras, Orissa, Assam, Nagpur, Punjab, Saurashtra, Patiala and
        the East Punjab States Union, Mysore, Hyderabad, Madhya Bharat and
        Travancore-Cochin. Along with the Attorney General for India, M.C.
        Setalvad were present the Advocate Generals of Bombay, Madras, Uttar
        Pradesh, Bihar, East Punjab, Orissa, Mysore, Hyderabad and Madhya
        Bharat. Present too, were Prime Minister, other Ministers, Ambassadors
        and diplomatic representatives of foreign States, a large number of
        Senior and other Advocates of the Court and other distinguished
        visitors. Taking care to ensure that the Rules of the Supreme Court were
        published and the names of all the Advocates and agents of the Federal
        Court were brought on the rolls of the Supreme Court, the inaugural
        proceedings were over and put under part of the record of the Supreme
        Court.</p> </div><br>
