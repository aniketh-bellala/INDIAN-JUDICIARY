      <h2 class="title2 inbox">Supreme Court:</h2>
      <p class="inbox">Supreme Court of India is the highest level of court of Indian juridical system which was established as per Part V, Chapter IV of the Constitution of India which endorses the concept of Supreme Court as the Federal Court to play the role of the guardian of the esteemed constitution of India with the status of the highest level of court in the status of appeal cases.This court is comprised of the Chief Justice along with 30 other judges to carry on the operation of the court. The proceeding of the Supreme Court is being heard only in the language of English.  The Supreme Court is governed by the Supreme Court Rules which was published in the year 1966.</p> </div><br>
      <div class="text box">
        <h2 class="title2 inbox">High Courts:</h2>
        <p class="inbox">High Courts are second Courts of Importance of the democracy of India. They are run by Article 141 of the Constitution of India. They are governed by the bindings conferred by the Supreme Court of India so far judgments and orders are concerned. The Supreme Court of India is the highest level of courts and is responsible for fixing the guidance to the High Courts set by precedence.The appointment of the judges of High Courts are being executed by the President of India with the consultation of the Chief Justice of India, the Chief Justice of High Court and the Governor of the state or union territory.</p> </div><br>
        <div class="text box">
            <h2 class="title2 inbox">District Courts and Village Courts:</h2>
            <p class="inbox">The basis of structuring of district courts in India is mainly depending upon the discretion of the state governments or the union territories. The structure of those courts are mainly made considering several factors like the number of cases, distribution of population, etc. Depending upon those factors the state government takes the decision of numbers of District Courts to be in operation for single district or clubbing together different adjacent districts.<br>The village courts are named as Lok Adalat or Nyaya Panchyat which means the service of justice extended to the villagers of India. This is the system for resolving disputes in micro level. The need of these courts is justified though the Madras Village Court Act of 1888. This act is followed by the development post 1935 in different provinces, which are re-termed as different states after the independence of 1947.</p> </div><br>
           

 
  
  
 


