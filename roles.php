<h2 class="title">Chief Justice of High Courts:</h2>
    <div class="text"><p>All matters of High Courts are addressed by Chief Justice of respective High Court. This includes : High Court superintends and controls all the subordinate courts. High Court can ask for details of proceedings from subordinate courts. High Court issues rules regarding the working of the subordinate courts. High Court can transfer any case from one court to another and can also transfer the case to High Courtself and decide the same.High Court can enquire into the records or other connected documents of any subordinate court. High Court can appoint High Courts administration staff and determine their salaries and allowances, and condHigh Courtions of service.</p></div>

    <h2 class="title">Registrar at Supreme Court of India:</h2>
        <div class="text"><p>The registrar is a chief executive officer of a judicial forum. They are in charge of the entire registry of the department.
In common law jurisdictions, registrars are usually judicial officers with the power to hear certain civil matters such as interlocutory applications and assessment of damages. In some jurisdictions, they may also hear trials of cases if both parties consent. Registrars are assisted by deputy-registrars, who in common law jurisdictions are sometimes called masters.
The registrar is the chief administrator of the department, normally they happen to be the head of the department. The posts of the registrar are generally created in a judicial forums such as tribunals, high courts and supreme courts and in educational universities. In the judicial forum they manage both administrative and judicial sides in departments under the authority of the president/chairman of the tribunals and chief justices of the high courts and supreme courts. The government may also appoint one or more assistant/deputy registrars.</p></div>
  
