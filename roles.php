      <h2 class="title">Chief Justice of India:</h2>
        <div class="text"><div class=" chief"><img src="images/chief.jpg" width="275px" height="190px"></div><p>The chief justice of India, officially the chief justice of the Supreme Court of India, is the chief judge of the Supreme Court of India as well as the highest-ranking officer of the Indian federal judiciary. The Constitution of India grants power to the president of India to appoint, in consultation with the outgoing chief justice, the next chief justice, who will serve until they reach the age of sixty-five or are removed by impeachmentAs head of the Supreme Court, the chief justice is responsible for the allocation of cases and appointment of constitutional benches which deal with important matters of law.[5] In accordance with Article 145 of the Constitution of India and the Supreme Court Rules of Procedure of 1966, the chief justice allocates all work to the other judges who are bound to refer the matter back to them (for re-allocation) in any case where they require it to be looked into by a larger bench of more judges.
On the administrative side, the chief justice carries out functions of maintenance of the roster, appointment of court officials and general and miscellaneous matters relating to the supervision and functioning of the Supreme Court.</p></div>
    
